# 1. Creating the Dictionary {Key: Value}
trip_budget = {
    "Train": 1500,
    "Hotel": 3000,
    "Food": 2000,
    "Sightseeing": 500
}

# 2. Accessing data by its Label (Key)
print(trip_budget["Hotel"])  # Prints: 3000

# 3. Adding a new item
trip_budget["Shopping"] = 1000

print(trip_budget)

